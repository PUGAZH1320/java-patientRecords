    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(name.getText().isEmpty() || past_history.getText().isEmpty()|| address.getText().isEmpty()|| number.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Missing Information");
        }else
        {
            try{
                Class.forName("com.mysql.jdbc.Driver");
                //int PatKey = 1;
                PatCount();
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/patient_db","root","");
                 java.sql.PreparedStatement add = Con.prepareStatement("insert into patientlist values(?,?,?,?,?,?,?)");
                add.setInt(1,patId);
                add.setString(2,name.getText());
                add.setString(3,number.getText());
                add.setString(4,address.getText());
                add.setString(5,age.getText());
                add.setString(6,gender.getSelectedItem().toString());
                add.setString(7,past_history.getText());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this,"Patient Added Sucessfully");
                Con.close();
                DisplayPatients();
            }catch(Exception e){
                e.printStackTrace(); 
            }
        }   
    }//GEN-LAST:event_jButton1MouseClicked
int Key = 0;
    private void patientlistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_patientlistMouseClicked
        DefaultTableModel model = (DefaultTableModel)patientlist.getModel();
        int MyIndex = patientlist.getSelectedRow();
        Key = Integer.valueOf(model.getValueAt(MyIndex, 0).toString());
        name.setText(model.getValueAt(MyIndex, 1).toString());
        number.setText(model.getValueAt(MyIndex, 2).toString());
        address.setText(model.getValueAt(MyIndex, 3).toString());
        age.setText(model.getValueAt(MyIndex, 4).toString());
        gender.setSelectedItem(model.getValueAt(MyIndex, 5).toString());
        past_history.setText(model.getValueAt(MyIndex, 6).toString());
    }//GEN-LAST:event_patientlistMouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        if(Key == 0)
        {
            JOptionPane.showMessageDialog(this,"Select The Patient");
        }else
        {
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/patient_db","root","");
                String Query = "update patientlist set name='"+name.getText()+"'"+",number='"+number.getText()+"'"+",address='"+address.getText()+"'"+",age='"+age.getText()+"'"+",gender='"+gender.getSelectedItem()+"'"+",past_history='"+past_history.getText()+"'"+" where ID="+Key;
                Statement Add = Con.createStatement();
                Add.executeUpdate(Query);
                JOptionPane.showMessageDialog(this,"Patient Updated Sucessfully");
                
                DisplayPatients();
            }catch(Exception e){
                e.printStackTrace(); 
            }
        } 
    }//GEN-LAST:event_jButton3MouseClicked
