    private void PatCount()
    {
       try{
           St1 = Con.createStatement();
           Rs1 = St1.executeQuery("select max(ID) from patientlist");
           Rs1.next();
           patId = Rs1.getInt(1)+1;
       } catch (Exception e)
       {
           e.printStackTrace();
       }
    }
